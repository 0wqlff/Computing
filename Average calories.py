#Average calories
Monday=int(input("How many calories did you consume on Monday? "))
Tuesday=int(input("How many calories did you consume on Tuesday? "))
Wednesday=int(input("How many calories did you consume on Wednesday? "))
Thursday=int(input("How many calories did you consume on Thursday? "))
Friday=int(input("How many calories did you consume on Friday? "))
Saturday=int(input("How many calories did you consume on Saturday? "))
Sunday=int(input("How many calories did you consume on Sunday? "))
Average=(Monday+Tuesday+Wednesday+Thursday+Friday+Saturday+Sunday)/7
print("You have consumed an average of",Average,"calories this week")
